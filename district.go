const (
	Province int = 1
	City     int = 2
)

type District struct {
	Level    int
	AdCode   int
	Name     string
	FullName string
	TelCode  string
	Postcode string
}

func (d *District) IsDirectCity() bool {
	return d.AdCode == 110000 || d.AdCode == 120000 || d.AdCode == 310000 || d.AdCode == 500000
}

func (d *District) IsCity() bool {
	return d.AdCode%100 == 0 && d.AdCode%10000 > 0
}

func (d *District) IsProvince() bool {
	return d.AdCode%10000 == 0
}

func (d *District) GetProvince() *District {
	if d.IsProvince() {
		return d
	}
	provCode := (d.AdCode / 10000) * 10000
	prov, _ := DistrictMapping[provCode]
	return &prov
}
